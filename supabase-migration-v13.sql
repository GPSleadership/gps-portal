-- ── 6. diagnostic_report_drafts — versioned AI-generated reports ──────────────
--
-- Each time Alex generates a report (via Claude API), a new version is saved here.
-- The highest version number with finalized_at IS NOT NULL is the active report.

CREATE TABLE IF NOT EXISTS diagnostic_report_drafts (
  id              UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  diagnostic_id   UUID NOT NULL REFERENCES diagnostics(id) ON DELETE CASCADE,

  version         INTEGER NOT NULL DEFAULT 1,
  -- Increments on each generation

  -- Structured report content (JSON for section-by-section editing)
  content_json    JSONB,
  -- Keys: executive_summary, trust_section, proactivity_section,
  --       productivity_section, impact_section, succession_section,
  --       open_ended_themes, development_priorities, closing

  -- Full markdown (used for PDF/DOCX export)
  raw_markdown    TEXT,

  -- Claude prompt snapshot (for reproducibility and debugging)
  prompt_snapshot TEXT,

  generated_at    TIMESTAMPTZ NOT NULL DEFAULT now(),
  finalized_at    TIMESTAMPTZ,
  -- Non-null = this version is the final report delivered to client

  finalized_by    TEXT DEFAULT 'alex@gpsleadership.org',

  UNIQUE (diagnostic_id, version)
);

