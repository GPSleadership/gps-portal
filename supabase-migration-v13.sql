  ON diagnostic_report_drafts (diagnostic_id, version DESC);


-- ── 8. ghl_export_view — safe GoHighLevel export ─────────────────────────────
--
-- Aggregates all scored diagnostic data into one flat row per diagnostic.
-- Used for periodic CSV export to GoHighLevel as off-system backup.
--
-- Fields prefixed gps_ per GHL custom field naming convention.
-- NEVER includes: verbatim text responses, individual rater scores,
-- successor self-report responses, or rater email addresses.
--
-- Score computation:
--   Trust Index     = avg(A1–A7) from non-self raters
--   Proactivity     = avg(B1–B6) from non-self raters
--   Productivity    = avg(C1–C6) from non-self raters
--   TP3 Index       = (Trust + Proactivity + Productivity) / 3
--   Overall Impact  = avg(D1) from non-self raters (1–10 scale)
--   Succession F1   = avg(F1) from non-self raters
--   Succession F2   = avg(F2) from non-self raters
--   G1 Custom       = avg(G1) from non-self raters
--   Self Trust      = avg(A1–A7) from self rater only
--   Self Proactivity= avg(B1–B6) from self rater only
--   Self Productivity=avg(C1–C6) from self rater only

CREATE OR REPLACE VIEW ghl_export_view AS
WITH rater_scores AS (
  -- Aggregate all rated question scores per diagnostic, excluding self
  SELECT
    dr.diagnostic_id,
    AVG(CASE WHEN drs.question_code IN ('A1','A2','A3','A4','A5','A6','A7') THEN drs.score END) AS trust_others,
    AVG(CASE WHEN drs.question_code IN ('B1','B2','B3','B4','B5','B6')      THEN drs.score END) AS proactivity_others,
    AVG(CASE WHEN drs.question_code IN ('C1','C2','C3','C4','C5','C6')      THEN drs.score END) AS productivity_others,
    AVG(CASE WHEN drs.question_code = 'D1'                                  THEN drs.score END) AS overall_impact,
    AVG(CASE WHEN drs.question_code = 'F1'                                  THEN drs.score END) AS succession_f1,
    AVG(CASE WHEN drs.question_code = 'F2'                                  THEN drs.score END) AS succession_f2,
    AVG(CASE WHEN drs.question_code = 'G1'                                  THEN drs.score END) AS succession_g1
  FROM diagnostic_raters dr
  JOIN diagnostic_responses drs ON drs.rater_id = dr.id
  WHERE dr.is_self = FALSE
    AND dr.completed_at IS NOT NULL
  GROUP BY dr.diagnostic_id
),
self_scores AS (
  -- Self-report scores only
  SELECT
    dr.diagnostic_id,
    AVG(CASE WHEN drs.question_code IN ('A1','A2','A3','A4','A5','A6','A7') THEN drs.score END) AS self_trust,
    AVG(CASE WHEN drs.question_code IN ('B1','B2','B3','B4','B5','B6')      THEN drs.score END) AS self_proactivity,
    AVG(CASE WHEN drs.question_code IN ('C1','C2','C3','C4','C5','C6')      THEN drs.score END) AS self_productivity
  FROM diagnostic_raters dr
  JOIN diagnostic_responses drs ON drs.rater_id = dr.id
  WHERE dr.is_self = TRUE
    AND dr.completed_at IS NOT NULL
  GROUP BY dr.diagnostic_id
),
rater_counts AS (
  SELECT
    diagnostic_id,
    COUNT(*) FILTER (WHERE is_self = FALSE)                   AS total_invited,
    COUNT(*) FILTER (WHERE is_self = FALSE AND completed_at IS NOT NULL) AS total_completed
  FROM diagnostic_raters
  GROUP BY diagnostic_id
)
SELECT
  d.id                    AS diagnostic_id,
  d.client_name           AS gps_contact_name,
  d.client_email          AS gps_contact_email,
  d.client_title          AS gps_role_title,
  d.client_org            AS gps_company,
  d.tier                  AS gps_diagnostic_tier,
  d.status                AS gps_diagnostic_status,
  d.start_date            AS gps_survey_start,
  d.close_date            AS gps_survey_close,
  d.debrief_completed_at  AS gps_debrief_date,
  d.plan_status           AS gps_plan_status,
  d.created_at            AS gps_diagnostic_created,

  -- Response stats
  rc.total_invited        AS gps_raters_invited,
  rc.total_completed      AS gps_raters_completed,
  ROUND(rc.total_completed::NUMERIC / NULLIF(rc.total_invited,0) * 100, 1)
                          AS gps_response_rate_pct,

  -- TP3 Scores (Others only, rounded to 2 decimal places)
  ROUND(rs.trust_others::NUMERIC, 2)       AS gps_trust_score,
  ROUND(rs.proactivity_others::NUMERIC, 2) AS gps_proactivity_score,
  ROUND(rs.productivity_others::NUMERIC, 2)AS gps_productivity_score,
  ROUND(((rs.trust_others + rs.proactivity_others + rs.productivity_others) / 3.0)::NUMERIC, 2)
                                           AS gps_tp3_index,
  ROUND(rs.overall_impact::NUMERIC, 2)     AS gps_overall_impact,

  -- Succession scores
  ROUND(rs.succession_f1::NUMERIC, 2)      AS gps_succession_f1,
  ROUND(rs.succession_f2::NUMERIC, 2)      AS gps_succession_f2,
  ROUND(rs.succession_g1::NUMERIC, 2)      AS gps_succession_g1_custom,

  -- Self-report scores (shown separately, never blended into TP3)
  ROUND(ss.self_trust::NUMERIC, 2)         AS gps_self_trust,
  ROUND(ss.self_proactivity::NUMERIC, 2)   AS gps_self_proactivity,
  ROUND(ss.self_productivity::NUMERIC, 2)  AS gps_self_productivity

FROM diagnostics d
LEFT JOIN rater_scores rs ON rs.diagnostic_id = d.id
LEFT JOIN self_scores  ss ON ss.diagnostic_id = d.id
LEFT JOIN rater_counts rc ON rc.diagnostic_id = d.id
ORDER BY d.created_at DESC;

COMMENT ON VIEW ghl_export_view IS
  'Flat aggregated view for GoHighLevel CSV export. Safe to share externally —
   contains NO verbatim responses, NO individual rater data, NO successor names.
   All score fields prefixed gps_ for GHL custom field naming convention.
   Run: SELECT * FROM ghl_export_view; then export as CSV.';


-- ── 9. updated_at trigger ─────────────────────────────────────────────────────
--
-- Keeps diagnostics.updated_at current on every row change.

CREATE OR REPLACE FUNCTION update_updated_at_column()
RETURNS TRIGGER AS $$
BEGIN
  NEW.updated_at = now();
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS set_diagnostics_updated_at ON diagnostics;
CREATE TRIGGER set_diagnostics_updated_at
  BEFORE UPDATE ON diagnostics
  FOR EACH ROW EXECUTE FUNCTION update_updated_at_column();

DROP TRIGGER IF EXISTS set_overrides_updated_at ON diagnostic_question_overrides;
CREATE TRIGGER set_overrides_updated_at
  BEFORE UPDATE ON diagnostic_question_overrides
  FOR EACH ROW EXECUTE FUNCTION update_updated_at_column();


