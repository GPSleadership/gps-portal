COMMENT ON TABLE diagnostic_raters IS
  'One row per invited rater plus one row for the leader self-assessment (is_self=true).
   completed_at is visible to Alex only — never surfaced to the leader to preserve anonymity.
   Leader portal shows only name/relationship/invited_at, never completion status.';

COMMENT ON COLUMN diagnostic_raters.token IS
  'UUID token used in survey URL. Each rater gets a unique link.
   Example URL: /diagnostic-survey.html?token=<token>
   is_self=true tokens land the leader in self-report mode (Section E shown, Section F/G hidden).';


-- ── 4. diagnostic_responses — per question per rater ─────────────────────────
--
-- One row per question per rater. Rated questions use score, open-ended use
-- text_response. A few questions may use both (e.g. a 1-5 with a follow-up text).
--
-- question_code values:
--   Section A (Trust):       A1–A7 (rated), A8–A10 (open)
--   Section B (Proactivity): B1–B6 (rated), B7–B10 (open)
--   Section C (Productivity):C1–C6 (rated), C7–C9 (open)
--   Section D (Impact):      D1 (rated 1–10), D2 (open)
--   Section E (Succession):  E1–E5 (open, self-report only)
--   Section F (Bench):       F1–F2 (rated 1–5), F3 (open)
--   Section G (Custom):      G1 (rated 1–5, AI-generated per diagnostic)
--
-- Note: Section E responses are stored BOTH here (for completeness) AND as
-- denormalized columns on the diagnostics row (for fast Claude API access).

CREATE TABLE IF NOT EXISTS diagnostic_responses (
  id              UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  diagnostic_id   UUID NOT NULL REFERENCES diagnostics(id) ON DELETE CASCADE,
  rater_id        UUID NOT NULL REFERENCES diagnostic_raters(id) ON DELETE CASCADE,

  question_code   TEXT NOT NULL,
  -- Format: letter + number, e.g. 'A1', 'B6', 'D1', 'G1', 'E3'

  score           NUMERIC(3,1),
  -- 1.0–5.0 for most rated questions (A1-A7, B1-B6, C1-C6, F1-F2, G1)
  -- 1.0–10.0 for D1 (Overall Impact)
  -- NULL for open-ended questions

  text_response   TEXT,
  -- Response text for open-ended questions
  -- NULL for pure rated questions

  created_at      TIMESTAMPTZ NOT NULL DEFAULT now(),

  UNIQUE (rater_id, question_code)
  -- One answer per question per rater
);

ALTER TABLE diagnostic_responses ENABLE ROW LEVEL SECURITY;
DROP POLICY IF EXISTS "Allow anon read diagnostic_responses"   ON diagnostic_responses;
DROP POLICY IF EXISTS "Allow anon insert diagnostic_responses" ON diagnostic_responses;
CREATE POLICY "Allow anon read diagnostic_responses"   ON diagnostic_responses FOR SELECT USING (true);
CREATE POLICY "Allow anon insert diagnostic_responses" ON diagnostic_responses FOR INSERT WITH CHECK (true);

COMMENT ON TABLE diagnostic_responses IS
  'One row per question per rater. Score for rated questions, text_response for open-ended.
   Section E (succession) responses also denormalized to diagnostics table for Claude API access.
   Never expose individual rater responses to the leader — only aggregated scores.';

COMMENT ON COLUMN diagnostic_responses.score IS
  'Numeric score. Scale depends on question:
   1–5: A1-A7, B1-B6, C1-C6, F1-F2, G1 (Strongly Disagree → Strongly Agree)
   1–10: D1 (Overall Leadership Impact)
   NULL: open-ended questions (A8-A10, B7-B10, C7-C9, D2, E1-E5, F3)';


