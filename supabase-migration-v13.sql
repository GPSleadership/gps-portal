-- ── 1. clients — add client_type ─────────────────────────────────────────────
--
-- coaching      : active coaching engagement (existing portal users)
-- diagnostic_only : paid diagnostic, no coaching contract
-- alumni        : completed GPS workshop, portal access for 90-day plan only
-- both          : has both an active diagnostic and active coaching

ALTER TABLE clients
  ADD COLUMN IF NOT EXISTS client_type TEXT NOT NULL DEFAULT 'coaching'
  CHECK (client_type IN ('coaching','diagnostic_only','alumni','both'));

COMMENT ON COLUMN clients.client_type IS
  'Determines which features are active and how the client appears in the coach portal.
   coaching = active coaching engagement.
   diagnostic_only = paid diagnostic, no coaching.
   alumni = post-workshop portal access only.
   both = active coaching + active diagnostic.';


-- ── 2. diagnostics — core diagnostic record ──────────────────────────────────
--
-- One row per engagement. Stores all metadata, pipeline status, self-report
-- succession responses, Claude-generated custom question, and plan status.

CREATE TABLE IF NOT EXISTS diagnostics (
  id                        UUID PRIMARY KEY DEFAULT gen_random_uuid(),

  -- Client link (nullable: diagnostic client may not be a coaching client yet)
  client_id                 UUID REFERENCES clients(id) ON DELETE SET NULL,

  -- Client identity (denormalized so diagnostic survives if client row is deleted)
  client_name               TEXT NOT NULL,
  client_email              TEXT NOT NULL,
  client_title              TEXT,
  client_org                TEXT,

  -- Tier and delivery mode
  tier                      TEXT NOT NULL DEFAULT 'standard'
                            CHECK (tier IN ('standard','pro')),
  email_delivery_mode       TEXT NOT NULL DEFAULT 'gps_sends'
                            CHECK (email_delivery_mode IN ('gps_sends','client_sends')),
  -- gps_sends   = portal auto-sends tokenized invite emails
  -- client_sends = portal generates email templates; Alex or client forwards manually

  -- Pipeline status (mirrors 8-milestone pipeline shown to leader)
  status                    TEXT NOT NULL DEFAULT 'setup'
                            CHECK (status IN (
                              'setup',               -- Step 1: Intake not complete
                              'intake_complete',     -- Step 1 done: intake call complete
                              'self_assessment_pending', -- Step 2: waiting on leader
                              'self_assessment_complete',-- Step 2 done: invites can go out
                              'survey_open',         -- Step 4: survey window active
                              'survey_closed',       -- Step 5: data locked
                              'report_draft',        -- Step 6: report being built
                              'report_final',        -- Step 6 done: report finalized
                              'debrief_complete',    -- Step 7 done
                              'plan_active'          -- Step 8: 90-day plan locked
                            )),

  -- Survey window
  start_date                DATE,   -- survey opens
  close_date                DATE,   -- survey closes (auto = start + 7 days)

  -- Intake
  intake_notes              TEXT,
  intake_transcript_url     TEXT,
  intake_completed_at       TIMESTAMPTZ,

  -- Self-report succession responses (Section E — stored here, not in responses table)
  -- Stored on the diagnostic record because they drive Claude custom question generation
  -- and are referenced in the report separately from rater data.
  self_three_year_vision          TEXT,
  self_future_self_capabilities   TEXT,
  self_immediate_successor_view   TEXT,
  self_successor_candidates       TEXT,
  self_successor_development_actions TEXT,
  self_assessment_completed_at    TIMESTAMPTZ,

  -- Custom rater question (Section G, G1) — generated by Claude from self_three_year_vision
  custom_g1_question        TEXT,
  custom_g1_generated_at    TIMESTAMPTZ,

  -- Rater invite tracking
  raters_finalized_at       TIMESTAMPTZ,  -- Alex approved rater list
  invites_sent_at           TIMESTAMPTZ,  -- bulk invite emails sent

  -- Survey window management
  survey_closed_at          TIMESTAMPTZ,  -- actual close (may differ from close_date)
  alert_t2_sent_at          TIMESTAMPTZ,  -- T-2 day alert (dedupe guard)

  -- Report
  report_generated_at       TIMESTAMPTZ,
  report_finalized_at       TIMESTAMPTZ,

  -- Debrief & 90-day plan
  debrief_completed_at      TIMESTAMPTZ,
  plan_status               TEXT NOT NULL DEFAULT 'not_started'
                            CHECK (plan_status IN ('not_started','draft','active','locked')),
  plan_draft_content        TEXT,
  plan_locked_at            TIMESTAMPTZ,
  plan_lock_source          TEXT CHECK (plan_lock_source IN ('auto_24h','manual')),
  -- auto_24h = cron locked it 24h after debrief_completed_at
  -- manual   = Alex or client locked it explicitly

  -- Leader portal access token (used in URL: /diagnostic-leader.html?token=...)
  leader_token              TEXT UNIQUE DEFAULT gen_random_uuid()::TEXT,
  leader_token_last_used_at TIMESTAMPTZ,

  created_at                TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at                TIMESTAMPTZ NOT NULL DEFAULT now()
);

ALTER TABLE diagnostics ENABLE ROW LEVEL SECURITY;
DROP POLICY IF EXISTS "Allow anon read diagnostics"   ON diagnostics;
DROP POLICY IF EXISTS "Allow anon insert diagnostics" ON diagnostics;
DROP POLICY IF EXISTS "Allow anon update diagnostics" ON diagnostics;
CREATE POLICY "Allow anon read diagnostics"   ON diagnostics FOR SELECT USING (true);
CREATE POLICY "Allow anon insert diagnostics" ON diagnostics FOR INSERT WITH CHECK (true);
CREATE POLICY "Allow anon update diagnostics" ON diagnostics FOR UPDATE USING (true);

COMMENT ON TABLE diagnostics IS
  '14-Day Executive Leadership Diagnostic — one row per engagement.
   Self-report succession responses stored here (Section E) because they
   drive Claude custom question generation before rater invites go out.
   All other survey responses are in diagnostic_responses.';

COMMENT ON COLUMN diagnostics.leader_token IS
  'UUID token used in the leader portal URL. Never expose to raters.
   Example URL: /diagnostic-leader.html?token=<leader_token>';

COMMENT ON COLUMN diagnostics.alert_t2_sent_at IS
  'Timestamp when T-2 day low-response alert was sent to Alex.
   Non-null = alert already fired. Cron checks this before sending to prevent duplicates.
   Fires when total completions < 7 two days before close_date.';

COMMENT ON COLUMN diagnostics.plan_lock_source IS
  'auto_24h = cron locked plan 24 hours after debrief_completed_at.
   manual = explicitly locked by Alex or client.';


-- ── 3. diagnostic_raters — invited raters ────────────────────────────────────
--
-- One row per invited rater, including the leader themselves (is_self = true).
-- The leader self-assessment uses a separate token that lands them in self-report mode.
-- Rater tokens are UUID strings appended as ?token=... in the survey URL.

CREATE TABLE IF NOT EXISTS diagnostic_raters (
  id              UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  diagnostic_id   UUID NOT NULL REFERENCES diagnostics(id) ON DELETE CASCADE,

  name            TEXT NOT NULL,
  email           TEXT NOT NULL,
  relationship    TEXT NOT NULL
                  CHECK (relationship IN (
                    'Self',
                    'Direct Report',
                    'Peer',
                    'Supervisor',
                    'Internal Partner',
                    'External Partner',
                    'Board / Investor',
                    'Key Customer'
                  )),

  -- Survey access token (used in URL: /diagnostic-survey.html?token=...)
  token           TEXT UNIQUE NOT NULL DEFAULT gen_random_uuid()::TEXT,

  is_self         BOOLEAN NOT NULL DEFAULT FALSE,
  -- TRUE only for the leader's own self-assessment row
  -- Self row is created automatically when invites are sent

  invited_at      TIMESTAMPTZ,
  completed_at    TIMESTAMPTZ,   -- set when survey is submitted

  -- Reminder tracking (mirrors existing survey_tokens pattern)
  reminder_1_sent_at  TIMESTAMPTZ,
  reminder_2_sent_at  TIMESTAMPTZ,
  email_bounced       BOOLEAN NOT NULL DEFAULT FALSE,
  bounced_at          TIMESTAMPTZ,

  created_at      TIMESTAMPTZ NOT NULL DEFAULT now()
);

ALTER TABLE diagnostic_raters ENABLE ROW LEVEL SECURITY;
